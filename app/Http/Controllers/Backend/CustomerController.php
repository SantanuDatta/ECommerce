    {
        $customer = User::find($id);
        if(!is_null($customer)){
            $notification = array(
                'alert-type'    => 'error',
                'message'       => 'User Removed Successfully!',
            );
            $customer->carts()->delete();
            $customer->orders()->delete();
            $customer->delete();
            return redirect()->route('customer.manage')->with($notification);
